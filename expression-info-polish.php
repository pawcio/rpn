<p>Niedostępny jest unarny operator znaku, przykładowo aby osiągnąć wartość <b>-2</b> należy wpisać <b>0 - 2</b>.</p>

<p>Dozwolone działania to:</p>
<ul>
	<li><b>+</b> dodawanie</li>
	<li><b>-</b> odejmowanie</li>
	<li><b>*</b> mnożenie</li>
	<li><b>/</b> dzielenie</li>
	<li><b>^</b> potęgowanie</li>
	<li><b>%</b> reszta z dzielenia</li>
</ul>

<p>dozwolone jest grupowanie działań w nawiasach (wyłącznie nawiasy okrągłe), działania są obliczane zgodnie z kolejnością wykonywania działań.</p>

<p>dotychczas umieszczone funkcje jednoargumentowe (w miejsce x mozna podać dowolne wyrażenie arytmetyczne):</p>
<ul>
	<li><b>abs(x)</b> - wartość bezwzględna liczby x</li>
	<li><b>exp(x)</b> - eksponens (równoważne z zapisem e ^ x)</li>
	<li><b>ln(x)</b> - logarytm naturalny z x</li>	
	<li><b>log(x)</b> - logarytm dziesiętny z x</li>
    <li><b>sgn(x)</b> - funkcja signum</li>
	<li><b>sqrt(x)</b> - pierwiastek kwadratowy z x</li>
	
	<li>funkcje trygonometryczne:
		<ul>
			<li><b>acos(x)</b> lub <b>arccos(x)</b> - arcus cosinus x</li>
			<li><b>actan(x)</b> lub <b>arcctg(x)</b> - arcus cotangens x</li>
			<li><b>asin(x)</b> lub <b>arcsin(x)</b> - arcus sinus x</li>			
			<li><b>atan(x)</b> lub <b>arctg(x)</b> - arcus tangens x</li>
			<li><b>cos(x)</b> - cosinus x</li>
			<li><b>cosec(x)</b> lub <b>csc(x)</b> - cosecans x</li>
			<li><b>ctg(x)</b> lub <b>ctan(x)</b> - cotangens x</li>
			<li><b>sec(x)</b> - secans x</li>
			<li><b>sin(x)</b> - sinus x</li>					
			<li><b>tg(x)</b> lub <b>tan(x)</b> - tangens x</li>
		</ul>
	</li>
</ul>

<p>Dodatkowo, w wyrażeniach można używać stałych:</p>
	<ul>
		<li><b>e</b> przybliżenie liczby e (2.718281828459).</li>
		<li><b>euler</b> przybliżenie stałej Eulera (0.57721566490153).</li>
		<li><b>pi</b> przybliżenie liczby π (3.1415926535898).</li>
	</ul>

